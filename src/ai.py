import os
from openai import OpenAI

client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))


def format_product_answer_ai(bcode: str) -> str:

    res = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[
            {
                "role": "system",
                "content": "คุณเป็นผู้ช่วยค้นหาสินค้าของร้าน ตอบเป็นภาษาไทยเสมอ แบบสั้น กระชับ และอ่านง่ายสำหรับแชต"
            },
            {
                "role": "user",
                "content": f"""
                            ผู้ใช้ค้นหา BCODE: {bcode}

                            คุณคือผู้ช่วยร้านค้าปลีกที่เป็นมิตร

                            ผู้ใช้ค้นหาสินค้า: {bcode}

                            ด้านล่างคือชือคอลัมเทียบกับความหมาย
                       
                            - BCODE คือ รหัสสินค้า
                            - ACODE คือ ชื่อย่อ
                            - DESCR ชื่อสินค้า
                            - BRAND ยี่ห้อ
                            - MODEL รุ่น
                            - PRICE1 ราคาหน่วยย่อย
                            - VENDOR ซื้อมาจาก
                            - MAIN หมวด
                            - UI1 หน่วยย่อย
                            - UI2 หน่วยใหญ่
                            - PRICEM1 ราคาหน่วยใหญ่
                            - MTP2 จำนวนต่อหน่วยใหญ่
                            - LOCATION1 สถานที่เก็บ
                            - LOCATION2 
                            - QTYOH2 จำนวนในสตอก
                            - REMARK หมายเหตุ
                            - SIZE1-3 ใน นอก หนา

                            ช่วยสรุปให้ผู้ใช้เข้าใจง่าย เป็นภาษาไทย
                            เขียนแบบเป็นมิตร อ่านง่าย ไม่ต้องใช้รูปแบบตาราง
                            ถ้าข้อมูลไหนไม่มีก็เว้นไปได้

                            แนวทาง:
                            - เริ่มด้วยประโยคสั้น ๆ แนะนำสินค้า
                            - แสดงข้อมูลสำคัญเป็น bullet point
                            - ใช้ emoji เล็กน้อยเพื่อให้อ่านง่าย
                            - ถ้าข้อมูลบางช่องว่าง ให้ข้ามไม่ต้องพูดถึง
                            - ใช้คำธรรมชาติ เช่น “ราคาขายย่อย”, “คงเหลือในสต็อก”
                            - ไม่ต้องเขียนชื่อ field database ตรง ๆ

                            ตัวอย่างสไตล์ที่ต้องการ:

                            📦 สินค้า: นม Kodomo Milk Bath  
                            🏷 รหัสสินค้า: 2201048  
                            🧴 ยี่ห้อ: Kodomo  
                            💰 ราคาขายย่อย: 89 บาท  
                            📦 จำนวนคงเหลือ: 24 ชิ้น  

                            ถ้ามีข้อมูลอื่นที่เป็นประโยชน์ สามารถเรียบเรียงเพิ่มได้

                            ตอบสั้น กระชับ เป็นกันเอง
     
                            """
            }
        ],
    )

    return res.choices[0].message.content